package com.digiturtle.minigames;

public class Uniform {
	
	private GItem helmet, chestplate, leggings, boots;

	public GItem getHelmet() {
		return helmet;
	}

	public void setHelmet(GItem helmet) {
		this.helmet = helmet;
	}

	public GItem getChestplate() {
		return chestplate;
	}

	public void setChestplate(GItem chestplate) {
		this.chestplate = chestplate;
	}

	public GItem getLeggings() {
		return leggings;
	}

	public void setLeggings(GItem leggings) {
		this.leggings = leggings;
	}

	public GItem getBoots() {
		return boots;
	}

	public void setBoots(GItem boots) {
		this.boots = boots;
	}

}
